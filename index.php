                <h2>Stroomcontract (PPA)</h2>
                <br>
                <h4>Als de PV-installatie stroom begint op te wekken zal deze stroom zelf gebruikt worden of ‘het net’ op gaan. In de praktijk wordt dit vaak met de huidige stroomleverancier geregeld. Dit is immers de simpelste oplossing.</h4>
                <p>Sinds 2018 is het technisch mogelijk om stroom in te kopen bij één partij en stroom te verkopen aan een andere partij. Dit gaat via een zogenaamd MLOEA punt. Afhankelijk van het huidige stroomcontract en de business case kan dit een interessante optie zijn om te onderzoeken. Voor stroominkoop wilt u de prijs natuurlijk zo laag mogelijk hebben en voor de verkoop van stroom de prijs zo hoog mogelijk. Dit tegengestelde belang zorgt ervoor dat één partij niet altijd de meest logische optie is. </p>
                <p><strong>Ook kan het zijn dat uw huidige leverancier simpelweg niet de beste deal biedt. Door ons uitgebreide netwerk kunnen wij hierin bemiddelen en adviseren.</strong></p>
